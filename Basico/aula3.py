"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print(1, 'Felipe de "Souza" Silva')

# Aspas duplas
print(2, "Felipe de 'Souza' Silva")

# Escape
print("Felipe de \"Souza\" Silva")

# r
print(r"Felipe de \"Souza\" Silva")
